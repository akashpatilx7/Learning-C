/*
Using Variables, we can store data inside the memory as well as give a particular name to that memory location to be refferred for future use. 
*/


#include<stdio.h>

int main()
{
  int number=25;
  char star='*';
  int age=22;

  // The value of any type of variable is "VARIABLE" !
  age=24;
  float pi=3.14;

  // a. Variables are Case Sensitive.
  int a=30;
  int A=40;

  // b. 1st Character is alphabet or '_'.
  int newAge=25;
  int _newAge=25;

  // c. No Comma/ Blank Space.

  // d. No other symbol other than '_'.

  // Underscore ("_") is the only Special Character that can be used, while writing a Varible Name.
  int final_price=100;

  return 0;
}